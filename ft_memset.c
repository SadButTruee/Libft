#include "libft.h"

void	*ft_memset(void *mas, int s, size_t k)
{
	unsigned char	*mas2;

	mas2 = mas;
	while (k > 0)
	{
		mas2[k - 1] = s;
		k--;
	}
	return (mas);
}	
